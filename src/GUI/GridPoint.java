package GUI;

import java.awt.Color;

/**
 *
 * @author rik_claessens
 */
public class GridPoint {
    
    private int x, y, size;
    private Color color;
    
    public GridPoint(int x, int y, int size, Color color){
        this.x = x;
        this.y = y;
        this.size = size;
        this.color = color;
    }

    public Color getColor() {
        return color;
    }
    
    public int getSize() {
        return size;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
    
    public void setColor(Color color){
        this.color = color;
    }
    
}
